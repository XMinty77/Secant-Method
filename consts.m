function C = consts()
    C.h = 1.054571817e-34; % Planck's reduced constant in J*s
    C.e = 1.602176634e-19; % Elementary charge in C
    C.r = 8.854187817e-12; % Permittivity of free space
    C.u = 1.66053907e-27; % Conversion factor from u to Kg
    C.c = 931.502; % Speed of light in MeV / u
    C.m = 4.002602 * 1.66053907e-27; % Mass of a helium nucleus in Kg
    C.k = 8.9875517862e9;
end